const month = 3

switch (month) {
    case 1:
        console.log("january");
        
        break;
    case 2:
        console.log("february");
        
        break;
    case 3:
        console.log("march");
        
        break;
    case 4:
        console.log("april");
        
        break;

    default:
        console.log("default case match");
        
        break;
}

//break - if break is comment out then after that case all other case will be executed until next break inside case, except default it will not execute default 